// 获取所有教程的平铺列表（用于进度追踪）
export function getAllTutorials() {
  const tutorials = []
  TUTORIALS.forEach(section => {
    section.children.forEach(tutorial => {
      tutorials.push({
        ...tutorial,
        sectionId: section.id,
        sectionTitle: section.title
      })
    })
  })
  return tutorials
}
